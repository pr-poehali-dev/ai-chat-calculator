function getTime() {
  return new Date().toLocaleTimeString("ru", { hour: "2-digit", minute: "2-digit" });
}

function rand<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

// Математические выражения
function tryMath(text: string): string | null {
  const clean = text
    .replace(/сколько будет|сколько равно|посчитай|вычисли|реши|решение|=\?|сколько это/gi, "")
    .replace(/плюс/gi, "+").replace(/минус/gi, "-")
    .replace(/умножить на|умножить|×/gi, "*")
    .replace(/разделить на|разделить|÷/gi, "/")
    .replace(/в степени|степень/gi, "**")
    .replace(/процент от/gi, "/100*")
    .replace(/квадратный корень из/gi, "Math.sqrt(")
    .trim();
  const expr = clean.match(/[\d\s+\-*/().,^%]+/)?.[0]?.trim().replace(",", ".");
  if (!expr || expr.length < 3) return null;
  try {
     
    const result = Function(`"use strict"; return (${expr})`)();
    if (typeof result === "number" && isFinite(result)) {
      const formatted = Number.isInteger(result) ? result : parseFloat(result.toFixed(6));
      return `${expr.replace(/\s+/g, " ")} = **${formatted}**`;
    }
  } catch {
    return null;
  }
  return null;
}

