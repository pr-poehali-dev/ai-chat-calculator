function getBotResponse(text: string, history: Message[]): string {
  const lower = text.toLowerCase().trim();

  // Попытка посчитать математику
  if (/\d/.test(text)) {
    const math = tryMath(text);
    if (math) return `Посчитал: **${math}**`;
  }

  // Поиск по ключевым словам
  for (const topic of SMART_TOPICS) {
    if (topic.keywords.some((kw) => lower.includes(kw))) {
      return topic.handler(text, history);
    }
  }

  return rand(DEFAULT_RESPONSES);
}

