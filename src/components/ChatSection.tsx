function getAIResponse(text: string): string {
  const lower = text.toLowerCase();
  for (const group of RESPONSE_MAP) {
    if (group.keywords.some((kw) => lower.includes(kw))) {
      return group.answers[Math.floor(Math.random() * group.answers.length)];
    }
  }
  return DEFAULT_ANSWERS[Math.floor(Math.random() * DEFAULT_ANSWERS.length)];
}

function getTime() {
  return new Date().toLocaleTimeString("ru", { hour: "2-digit", minute: "2-digit" });
}

export default function ChatSection() {
  const [messages, setMessages] = useState<Message[]>([
    {
      id: 1,
      role: "ai",
      text: "Привет! Меня зовут Рома — ваш личный ИИ-помощник 👋 Я знаю много о разных темах: наука, кулинария, финансы, психология, путешествия и многое другое. Что вы хотели бы узнать сегодня?",
      time: getTime(),
    },
  ]);
  const [input, setInput] = useState("");
  const [isTyping, setIsTyping] = useState(false);
  const bottomRef = useRef<HTMLDivElement>(null);
  const inputRef = useRef<HTMLTextAreaElement>(null);

  useEffect(() => {
    bottomRef.current?.scrollIntoView({ behavior: "smooth" });
  }, [messages, isTyping]);

  const sendMessage = () => {
    const text = input.trim();
    if (!text) return;

    const userMsg: Message = { id: Date.now(), role: "user", text, time: getTime() };
    setMessages((prev) => [...prev, userMsg]);
    setInput("");
    setIsTyping(true);

    const delay = 900 + Math.random() * 1000;
    setTimeout(() => {
      const aiMsg: Message = {
        id: Date.now() + 1,
        role: "ai",
        text: getAIResponse(text),
        time: getTime(),
      };
      setMessages((prev) => [...prev, aiMsg]);
      setIsTyping(false);
    }, delay);
  };

  const handleKey = (e: React.KeyboardEvent) => {
    if (e.key === "Enter" && !e.shiftKey) {
      e.preventDefault();
      sendMessage();
    }
  };

  const clearChat = () => {
    setMessages([
      {
        id: Date.now(),
        role: "ai",
        text: "Чат очищен. Привет снова! Я Рома — готов к новому разговору. Что вы хотели бы узнать?",
        time: getTime(),
      },
    ]);
  };

  return (
    <div className="flex flex-col h-full">
      {/* Header */}
      <div className="flex items-center gap-3 px-6 py-4 border-b border-[var(--nova-border)]">
        <div className="relative">
          <div className="w-10 h-10 rounded-full bg-gradient-to-br from-[#7c6df8] to-[#4f46e5] flex items-center justify-center text-white font-bold text-sm">
            Р
          </div>
          <div className="absolute -bottom-0.5 -right-0.5 w-3 h-3 rounded-full bg-emerald-400 border-2 border-[#0b0c14]" />
        </div>
        <div>
          <div className="font-semibold text-[var(--nova-text)] text-sm">Рома</div>
          <div className="text-xs text-emerald-400">онлайн · ИИ-помощник</div>
        </div>
        <div className="ml-auto flex items-center gap-2">
          <button
            onClick={clearChat}
            title="Очистить чат"
            className="p-2 rounded-lg glass glass-hover text-[var(--nova-text-muted)] hover:text-[var(--nova-text)] transition-colors"
          >
            <Icon name="RotateCcw" size={15} />
          </button>
        </div>
      </div>

      {/* Messages */}
      <div className="flex-1 overflow-y-auto px-6 py-4 space-y-4">
        {messages.map((msg, i) => (
          <div
            key={msg.id}
            className={`flex gap-3 animate-fade-in ${msg.role === "user" ? "flex-row-reverse" : ""}`}
            style={{ animationDelay: `${Math.min(i * 0.04, 0.3)}s` }}
          >
            {msg.role === "ai" && (
              <div className="w-8 h-8 rounded-full bg-gradient-to-br from-[#7c6df8] to-[#4f46e5] flex items-center justify-center text-white text-xs font-bold flex-shrink-0 mt-1">
                Р
              </div>
            )}
            <div className={`max-w-[75%] ${msg.role === "user" ? "items-end" : "items-start"} flex flex-col gap-1`}>
              <div
                className={`px-4 py-3 rounded-2xl text-sm leading-relaxed ${
                  msg.role === "user"
                    ? "bg-gradient-to-br from-[#7c6df8] to-[#5b50d6] text-white rounded-tr-sm"
                    : "glass text-[var(--nova-text)] rounded-tl-sm"
                }`}
              >
                {msg.text}
              </div>
              <div className="text-[10px] text-[var(--nova-text-muted)] px-1">{msg.time}</div>
            </div>
          </div>
        ))}

        {isTyping && (
          <div className="flex gap-3 animate-fade-in">
            <div className="w-8 h-8 rounded-full bg-gradient-to-br from-[#7c6df8] to-[#4f46e5] flex items-center justify-center text-white text-xs font-bold flex-shrink-0">
              Р
            </div>
            <div className="glass px-4 py-3 rounded-2xl rounded-tl-sm flex items-center gap-1.5">
              <div className="typing-dot" />
              <div className="typing-dot" />
              <div className="typing-dot" />
            </div>
          </div>
        )}
        <div ref={bottomRef} />
      </div>

      {/* Quick prompts */}
      {messages.length <= 1 && (
        <div className="px-6 pb-2 flex gap-2 flex-wrap">
          {["Расскажи о себе", "Дай совет по финансам", "Что посмотреть вечером?", "Помоги с рецептом"].map((prompt) => (
            <button
              key={prompt}
              onClick={() => {
                setInput(prompt);
                setTimeout(() => {
                  const fakeEvent = { key: "Enter", shiftKey: false, preventDefault: () => {} } as React.KeyboardEvent;
                  setInput(prompt);
                }, 0);
              }}
              className="text-xs px-3 py-1.5 glass glass-hover rounded-xl text-[var(--nova-text-muted)] hover:text-[var(--nova-indigo)] transition-colors"
            >
              {prompt}
            </button>
          ))}
        </div>
      )}

      {/* Input */}
      <div className="px-6 py-4 border-t border-[var(--nova-border)]">
        <div className="flex gap-3 items-end glass rounded-2xl px-4 py-3">
          <textarea
            ref={inputRef}
            value={input}
            onChange={(e) => setInput(e.target.value)}
            onKeyDown={handleKey}
            placeholder="Спросите у Ромы что угодно..."
            rows={1}
            className="flex-1 bg-transparent text-[var(--nova-text)] placeholder:text-[var(--nova-text-muted)] text-sm resize-none outline-none leading-relaxed max-h-32 overflow-y-auto"
            style={{ scrollbarWidth: "none" }}
          />
          <button
            onClick={sendMessage}
            disabled={!input.trim()}
            className="w-8 h-8 rounded-xl bg-[var(--nova-indigo)] disabled:opacity-30 flex items-center justify-center text-white transition-all hover:bg-[#6b5ce7] active:scale-95 flex-shrink-0"
          >
            <Icon name="ArrowUp" size={15} />
          </button>
        </div>
        <div className="text-[10px] text-[var(--nova-text-muted)] text-center mt-2">
          Enter — отправить · Shift+Enter — новая строка
        </div>
      </div>
    </div>
  );
}
